public class DataTypes {
    public static void main(String[] args) {
        int value=10;
        boolean flag=true;
        char c='a';
        long num=12345678L;
        short n=123;
        float f=1.2f;
        double d=12.45;
        byte b=1;
        System.out.println("This value is from int variable "+value);
        System.out.println("This value is from float variable "+f);
        System.out.println("This is a character "+c);
        System.out.println("This is a boolean value "+flag);
        System.out.println("This is a long value "+num);
        System.out.println("This is a short value "+n);
        System.out.println("This is a byte value "+b);
        System.out.println("This is a double value "+d);

        //Primitive Data Types -- int, char, float,double, long, short, boolean, byte
        //int - size - 4 byte, default value - 0
        //float - size - 4 byte, default value - 0.0f
        //char - size - 2 byte, default value - '\u0000'
        //boolean - size - 1 byte, default value - false
        //double - size - 8 byte, default value - 0.0
        //byte - size - 1 byte, default value - 0
        //short - size - 2 byte, default value - 0
        //long - size - 8 byte, default value - 0L

        //Non Primitive Data Type --  String, array
    }
}
